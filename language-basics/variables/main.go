// Variable Declaration

package main

import "fmt"

func main() {

	/*
		Data type should be told to the compiler in following delclaration syntax
	*/

	var name string = "Eshban"
	fmt.Println(name)

	/*
		Following delclaration syntax doesn't require explicit data type
	*/

	name2 := "Suleman"
	fmt.Println(name2)

}
